trait Prop {
  type FailedCase = String
  type SuccessCount = Int
  def check: Either[(FailedCase, SuccessCount), SuccessCount]
  def &&(b: Prop): Prop = new Prop {
    override def check = {
      Prop.this.check match {
        case Left(fail1) =>
          b.check match {
            case Left(fail2) =>
              Left((fail1._1 + fail2._1, fail1._2 + fail2._2))
            case Right(succeed2) =>
              Left(fail1._1, fail1._2 + succeed2)
          }
        case Right(succeed1) =>
          b.check match {
            case Left(fail2) =>
              Left(fail2._1, succeed1 + fail2._2)
            case Right(succeed2) =>
              Right(succeed1 + succeed2)
          }
      }
    }
  }
}



